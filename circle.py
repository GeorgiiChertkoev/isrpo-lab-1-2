import math


def area(r):
    '''
    Returns area of a circle with radius r

    Arguments:
        r (int or float) - radius of a circle
    '''
    
    return math.pi * r * r


def perimeter(r):
    '''
    Returns perimeter of a circle with radius r

    Arguments:
        r (int or float) - radius of a circle
    '''

    return 2 * math.pi * r

