def area(a, b):
    '''
    Returns area of a rectangle with sides a and b

    Arguments:
        a (int) - length of side
        b (int) - length of side adjacent to a
    '''
    
    return a * b

def perimeter(a, b):
    '''
    Returns perimetr of a rectangle with sides a and b

    Arguments:
        a (int) - length of side
        b (int) - length of side adjacent to a
    '''

    return 2 * (a + b)